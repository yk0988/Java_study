package ch04.exam;

public class count {
	    // 1~10까지 합을 출력하는 프로그램
	    public static void main(String[] args) {
	        int sum = 0;  // 합을 저장할 변수
	        
	        // 1부터 10까지 반복
	        for (int i = 1; i <= 10; i++) {
	            sum += i;  // sum에 i를 더함
	        }
	        
	 
	        System.out.println("1부터 10 합: " + sum);
	     }
		}
	

		
